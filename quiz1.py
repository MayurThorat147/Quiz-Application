def display_question(question_data):
    print(question_data["question"])
    for i, option in enumerate(question_data["options"]):
        print(f"{i + 1}. {option}")
    user_choice = int(input("Enter your choice (1, 2, 3, 4): ")) - 1
    
    if user_choice == question_data["correct_answer"]:
        print("⭐Correct!⭐\n")
        return True
    else:
        print(f"This is not the correct answer. The correct answer is: {question_data['options'][question_data['correct_answer']]}\n")
        return False

def main():
    print("Welcome to Programming Quiz Application!")

    for subject in quiz_data.keys():
        print(f"{subject}")
    selected_subject = input("Enter the subject you want to participate in: ")
    if selected_subject in quiz_data:
        print(f"\n--- {selected_subject} Section ---")
        questions = quiz_data[selected_subject]
        score = 0
        for index, question in enumerate(questions):
            print(f"\nQuestion {index + 1}:")
            if display_question(question):
                score += 1
        print(f"You scored {score}/{len(questions)} in the {subject} section.\n")
    else:
        print("Invalid subject selection.")
        
if __name__ == "__main__":
    main()
